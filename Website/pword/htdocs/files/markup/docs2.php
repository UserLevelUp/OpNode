  <p> <strong> The Idea of Reuse - The idea of reuse, and how pWord makes it simple.</strong> 
  </p>
  <p> Reuse in pWord is as simple as placing common items under a common theme. 
    For instance... all of your websites could go under a title such as &quot;WebSites&quot;. 
    If it's financial, the records would go under the name of the owner or facility, 
    the department, the type of payment or receivables etc... By organizing the 
    items under a tree, it gives you control over the data, and allows you to 
    find it easier in a mountain of data.</p>
  <p> <strong> The Idea of Manupulation</strong> </p>
  <p> Manipulating nodes requires you to practice with the right click button's 
    &quot;get node&quot; menu item. Get node get the selected node and all sub 
    nodes. This allows you to put the node or insert the node where ever you please 
    in the treeview.</p>
  <p> Once you give something a name, if you need that node, and all of it's subnodes, 
    it's possible to transfer that information with pWord. Just right click and 
    select &quot;Get Node&quot; it will get the node and then it will allow you 
    to either place that node into another node, or insert it in front of another 
    node.</p>
  <p> Practice makes perfect, so try make a few nodes, then use the get node after 
    right click the node. Then use the put node in or insert node into and see 
    what happens. You can manipulate huge chunks of data quickly and easily, and 
    reorganize the structure of your tree. Once you have a structure you are happy 
    with, you can export the data to xml and save your data to a pwd file.</p>
  <p> <strong> The Idea of Manupulation</strong> </p>
  <p> The left right control allows you to create many different treesViews. Depending 
    on the topic, they may or may not relate to another treeView. As you add more 
    and more treeViews, you can find them by select the left and right arrows 
    on the control.</p>
  <p> <strong> The AutoHide Feature</strong> </p>
  <p> The Auto Hide feature is a very powerful tool. Using the autohide feature 
    enables you to work on other applications, and transfer data using pWord. 
    This is especially important when using remote sessions. pWord allows for 
    complex manipulation and drag and drop operations to be performed to applications 
    that reside underneath pWord. Since pWord hides automatically, it allows the 
    drag and drop operation or copy and paste operation to proceed while pWord 
    is hiding. </p>
  <p> Once you move the mouse to the right side of the screen, the pWord app will 
    reappear. This is correct behavior for this application. If you have other 
    important work to perform that causes conflict, you can minize pWord.</p>
  <p> If you minimize pWord while it is in autohide mode, pWord will disappear 
    from teh task bar and hide in the notifcation area. When it does this, you'll 
    have to call pWord from the notifcation area and select show to use it again.</p>
  <p> <strong> Opening and Saving pWord Files</strong> </p>
  <p> Opening and Saving files to pWord is as simple as selecting the file menu 
    items and Selecting Save or if you have already saved a file you can open 
    it by selecting Open. Once you have completed that operation, you will need 
    to select a filename, or enter a filename. The suffix of any pWord file should 
    end in .pwd for password. Since pWord is a password and scriptiong utility. 
    Be careful not to confuse the export xml feature with the save feature, as 
    you may lose all your work.</p>
